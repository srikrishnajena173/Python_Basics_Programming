# So, here lets explore the agumented assigenment operator :- 

# Example 1 :- 
some_value = 5
some_value = some_value + 2
print(some_value)
# O/P :- 7

# The shortcut way to do this is 
some_value_short = 5
some_value_short += 2
print(some_value_short)
# O/P :- 7

# OR

some_value_short -= 2
print(some_value_short)
# O/P :- 5