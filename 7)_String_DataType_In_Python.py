# Lets explore the String data type 

# Example 1 :-
print(type('sample string value'))
# O/P :- <class 'str'>

# Eample 2 :- store a long string value
my_long_string = '''my name
is 
sir 
whats 
your name'''
print(my_long_string) # so here  three single quotes ''' for long string 
# O/P :- 
# my name
# is
# sir
# whats
# your name

# Example 3 :-  String concatenation  
first_name = 'sri'
last_name = 'jena'
print(first_name + ' ' + last_name)
# O/P :- sri jena

# OR 

print('My full name is ' + first_name + ' ' + last_name)
# O/P :- My full name is sri jena